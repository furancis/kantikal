import { expect, test } from '@playwright/test'

test('renders the visual Suno workflow shell', async ({ page }) => {
  await page.goto('/')
  await expect(page.getByRole('heading', { name: 'Visual music generation operating app' })).toBeVisible()
  await expect(page.getByRole('heading', { name: 'Perfect lipsync gate' })).toBeHidden()
  await expect(page.getByRole('button', { name: /Open music video lane/i })).toBeDisabled()
  await page.getByRole('textbox', { name: 'Brief' }).fill('Neon khaliji club hook')
  await page.getByRole('textbox', { name: 'Lyrics' }).fill('Verse pre chorus')
  await page.getByRole('textbox', { name: 'Style' }).fill('Gulf percussion and electro-pop')
  await page.getByRole('textbox', { name: 'Voice' }).fill('Consented bright tenor persona')
  await page.getByRole('button', { name: /Generate mock Suno batch/i }).click()
  await page.getByRole('button', { name: /Neon khaliji club hook v2/i }).click()
  await page.getByRole('button', { name: /Version comparison/i }).click()
  await page.getByRole('button', { name: /Rate mock-track-2 as taste match/i }).click()
  await page.getByRole('button', { name: /Compare mock-track-1 vs mock-track-2/i }).click()
  await expect(page.getByText(/Taste score: 5/i)).toBeVisible()
  await expect(page.getByText(/winner mock-track-2/i)).toBeVisible()
  await page.getByRole('button', { name: /Song Lab/i }).click()
  await page.getByRole('button', { name: /Open Song Lab/i }).click()
  await expect(page.getByRole('heading', { name: 'Song Lab' })).toBeVisible()
  await expect(page.getByText(/Source track: mock-track-2/i)).toBeVisible()
  await page.getByRole('button', { name: /Lock hook region/i }).click()
  await page.getByRole('button', { name: /Queue replace section/i }).click()
  await expect(page.getByText(/Hook locked/i)).toBeVisible()
  await expect(page.getByText(/songlab-edit-1 queued/i)).toBeVisible()
  await page.getByRole('button', { name: /Save selected to local library/i }).click()
  await expect(page.getByRole('heading', { name: 'Local library' })).toBeVisible()
  await expect(page.getByText(/Provider library API unsupported/i).last()).toBeVisible()
  await expect(page.getByText(/mock-track-2 saved locally/i)).toBeVisible()
  await page.getByRole('button', { name: /Run API action Create song/i }).click()
  await expect(page.getByRole('status')).toContainText(/Create song/i)
  await page.getByRole('button', { name: /Run API action Library list\/search/i }).click()
  await expect(page.getByRole('status')).toContainText(/Library list\/search/i)
  await page.getByRole('button', { name: /Job queue/i }).click()
  await expect(page.getByRole('heading', { name: 'Job queue' })).toBeVisible()
  await expect(page.getByText(/Create song completed/i).last()).toBeVisible()
  await expect(page.getByText(/Library list\/search unsupported/i).last()).toBeVisible()
  await expect(page.getByRole('button', { name: /Open music video lane/i })).toBeEnabled()
  await page.getByRole('button', { name: /Open music video lane/i }).click()
  await expect(page.getByRole('heading', { name: 'Perfect lipsync gate' })).toBeVisible()
  await expect(page.getByLabel(/Segment drift QA result/i)).toContainText(/Pending/i)
  await expect(page.getByLabel(/Post-stitch sync QA result/i)).toContainText(/Pending/i)
  await expect(page.getByText(/Music video source: mock-track-2/)).toBeVisible()
  await page.getByRole('button', { name: /Create video release pack/i }).click()
  await expect(page.getByRole('alert')).toContainText(/blocked until lipsync QA passes/i)
  await page.getByRole('button', { name: /Run lipsync QA/i }).click()
  await expect(page.getByLabel(/Segment drift QA result/i)).toContainText(/Repair required/i)
  await expect(page.getByLabel(/Post-stitch sync QA result/i)).toContainText(/Repair required/i)
  await page.getByRole('button', { name: /Queue repair pass/i }).click()
  await expect(page.getByText(/repair-1 queued/i)).toBeVisible()
  await page.getByRole('button', { name: /Run lipsync QA/i }).click()
  await expect(page.getByLabel(/Video export gate state/i)).toContainText(/Video export ready/i)
  await expect(page.getByText(/repair-1 applied/i)).toBeVisible()
  await expect(page.getByRole('button', { name: /Run lipsync QA/i })).toBeDisabled()
  await expect(page.getByRole('button', { name: /Open music video lane/i })).toBeDisabled()
  await page.getByRole('button', { name: /Create video release pack/i }).click()
  await expect(page.getByRole('heading', { name: 'Release pack' })).toBeVisible()
  await expect(page.getByText(/Video included/i)).toBeVisible()
  await expect(page.getByRole('heading', { name: 'Package contents' })).toBeVisible()
  await expect(page.getByText(/lipsync-approved video/i)).toBeVisible()
  await expect(page.getByRole('heading', { name: 'Provenance receipts' })).toBeVisible()
  await expect(page.getByText(/lipsync-qa-passed/i)).toBeVisible()
  await page.getByRole('button', { name: /Plan archive-first cleanup/i }).click()
  await expect(page.getByText(/Cleanup archived/i)).toBeVisible()
  await expect(page.getByText(/archive-1/i)).toBeVisible()
  await page.getByRole('button', { name: /Apply cleanup/i }).click()
  await expect(page.getByText(/Cleanup applied/i)).toBeVisible()
  await expect(page.getByRole('button', { name: /Neon khaliji club hook v1/i })).toBeHidden()
  await page.getByRole('button', { name: /Restore archived tracks/i }).click()
  await expect(page.getByText(/Cleanup restored/i)).toBeVisible()
  await expect(page.getByRole('button', { name: /Neon khaliji club hook v1/i })).toBeVisible()
})
